<p>Aktivitas Motorik Kasar</p>
<p>
    Untuk mengoptimalkan kemampuan motorik anak, orang tua dapat bermain bersama anak dengan 'permainan yang bertujuan' atau lebih dikenal dengan goal oriented play. Goal oriented play dilaporkan dapat meningkatkan keterampilan motorik pada anak usia dini pada anak usia 4-6 tahun. Goal oriented play dapat mengembangkan elemen motorik seperti kekuatan, daya tahan, kelincahan, kecepatan, keseimbangan, dan koordinasi. Permainan yang berorientasi pada tujuan dapat mengembangkan keterampilan multilateral, membantu membangun kemampuan dasar. Bermain aktif ditandai dengan aktivitas otot berulang, yang meningkatkan kekuatan, kecepatan, kelincahan, dan fleksibilitas otot. Efeknya adalah peningkatan kemampuan motorik anak. Pelatihan keterampilan motorik anak usia dini terbukti bermanfaat untuk literasi fisik hingga dewasa, karena aspek-aspek ini berkontribusi untuk membentuk kedewasaan yang mandiri. Orang tua dan anak dapat mencoba aktivitas-aktivitas di bawah ini untuk melatih kemampuan motorik anak
</p>
